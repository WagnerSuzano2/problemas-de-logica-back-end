let raioDoCirculo = 1;

const areaDoCirculo = Math.PI * Math.pow(raioDoCirculo, 2);
console.log(
  `A área do círculo com raio ${raioDoCirculo} é: ${areaDoCirculo.toFixed(2)}`
);

const perimetroDoCirculo = 2 * Math.PI * raioDoCirculo;
console.log(
  `O perímetro do círculo com raio ${raioDoCirculo} é: ${perimetroDoCirculo.toFixed(
    2
  )}`
);
